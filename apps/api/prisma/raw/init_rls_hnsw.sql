-- Project-scoped tables: filter by parent project.workspace_id.
CREATE POLICY requirements_workspace_isolation            ON requirements
  USING (EXISTS (SELECT 1 FROM projects p WHERE p.id = requirements.project_id
    AND p.workspace_id = current_setting('app.workspace_id')::uuid));

