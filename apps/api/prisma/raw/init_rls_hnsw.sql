-- kb_documents may have NULL project_id (workspace-scoped). Allow when the
-- document is workspace-scoped (project_id IS NULL is intentional and admin
-- gating is at app layer) OR when the parent project is in the workspace.
CREATE POLICY kb_documents_workspace_isolation            ON kb_documents
  USING (
    project_id IS NULL
    OR EXISTS (SELECT 1 FROM projects p WHERE p.id = kb_documents.project_id
      AND p.workspace_id = current_setting('app.workspace_id')::uuid)
  );

-- Join tables: transitive via parent.
