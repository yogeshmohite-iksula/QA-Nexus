-- ─────────────────────────────────────────────────────────────────────────────
-- 4. audit_log APPEND-ONLY ENFORCEMENT
-- ─────────────────────────────────────────────────────────────────────────────
-- Defence in depth on top of the app-layer rule: no UPDATE, no DELETE, ever.

CREATE OR REPLACE FUNCTION reject_audit_log_mutation()
RETURNS trigger AS $$
BEGIN
  RAISE EXCEPTION 'audit_log is append-only — UPDATE/DELETE rejected (op=%, table=%)',
    TG_OP, TG_TABLE_NAME;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER audit_log_block_update
  BEFORE UPDATE ON audit_log
  FOR EACH ROW EXECUTE FUNCTION reject_audit_log_mutation();

CREATE TRIGGER audit_log_block_delete
  BEFORE DELETE ON audit_log
  FOR EACH ROW EXECUTE FUNCTION reject_audit_log_mutation();

